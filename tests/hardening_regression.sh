    printf '%s\n' 'Wired connection 1'
    printf '%s\n' 'Office LAN'
    exit 0
fi
printf '%s\n' "$*" >> "${TMP_NMCLI_ARGS_FILE}"
exit 0
EOF
    chmod +x "${bindir}/nmcli"
    PATH="${bindir}:${PATH_BACKUP}"
    export TMP_NMCLI_ARGS_FILE="${args_file}"
    network_write_networkmanager_dns "223.5.5.5" "1.1.1.1"
