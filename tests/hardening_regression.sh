#!/usr/bin/env bash
printf '%s\n' "$*" >> "${TMP_PUBLIC_IP_CURL_LOG}"
case "$*" in
  *"https://service-a.example/ip"*)
    exit 1
    ;;
  *"https://service-b.example/ip"*)
    printf '%s\n' '203.0.113.9'
    exit 0
    ;;
esac
