assert_eq() {
    local expected="$1"
    local actual="$2"
    local message="$3"
    if [[ "${expected}" != "${actual}" ]]; then
        echo "断言失败: ${message}" >&2
        echo "期望: ${expected}" >&2
        echo "实际: ${actual}" >&2
        exit 1
    fi
}

assert_contains() {
    local needle="$1"
    local haystack="$2"
    local message="$3"
    if [[ "${haystack}" != *"${needle}"* ]]; then
        echo "断言失败: ${message}" >&2
        echo "未找到: ${needle}" >&2
        echo "实际内容: ${haystack}" >&2
        exit 1
    fi
}

assert_not_contains() {
    local needle="$1"
    local haystack="$2"
    local message="$3"
    if [[ "${haystack}" == *"${needle}"* ]]; then
        echo "断言失败: ${message}" >&2
        echo "不应包含: ${needle}" >&2
        echo "实际内容: ${haystack}" >&2
        exit 1
    fi
}

assert_success() {
    local message="$1"
    shift
    if ! "$@"; then
        echo "断言失败: ${message}" >&2
        exit 1
    fi
}

assert_failure() {
    local message="$1"
    shift
    if "$@"; then
        echo "断言失败: ${message}" >&2
        exit 1
    fi
}

